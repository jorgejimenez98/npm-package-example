/**
* Add the numbers
* @param a Number one
* @param b Number two
* @returns Result  a + b
*/
const add = (a: number, b: number): number => a + b
/**
* Substract the numbers
* @param a Number one
* @param b Number two
* @returns Result  a - b
*/
const substract = (a: number, b: number): number => a - b
/**
* Multiply the numbers
* @param a Number one
* @param b Number two
* @returns Result  a * b
*/
const multiply = (a: number, b: number): number => a * b

export {
    add,
    substract,
    multiply
}