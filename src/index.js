/**
* Add the numbers
* @param a Number one
* @param b Number two
* @returns Result  a + b
*/
var add = function (a, b) { return a + b; };
/**
* Substract the numbers
* @param a Number one
* @param b Number two
* @returns Result  a - b
*/
var substract = function (a, b) { return a - b; };
/**
* Multiply the numbers
* @param a Number one
* @param b Number two
* @returns Result  a * b
*/
var multiply = function (a, b) { return a * b; };
export { add, substract, multiply };
